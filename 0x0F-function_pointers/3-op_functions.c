#include "3-calc.h"

/**
 *op_add - does a + b
 *@a: first int
 *@b: second int
 *Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - does a minus b
 *@a: first int
 *@b: second int
 *Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - does a times b
 *@a: first int
 *@b: second int
 *Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - does a divided by b
 *@a: first int
 *@b: second int
 *Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - does a mod b
 *@a: first int
 *@b: second int
 *Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
